class calculator{
    int num1=20;
    int num2=10;

    void add(){
        int sum = num1+num2;
        System.out.println("Addition of two numbers :"+sum);
    }

    void sub(){
        int diff = num1-num2;
        System.out.println("Difference of two numbers :"+diff);
    }

    void mul(){
        int multi = num1*num2;
        System.out.println("Multiplication of two numbers :"+multi);
    }

    void div(){
        int divi = num1/num2;
        System.out.println("Division of two numbers :"+divi);
    }

}